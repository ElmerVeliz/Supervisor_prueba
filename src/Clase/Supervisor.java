package Clase;

public class Supervisor {
	
	private int codsuper;
	private String nomsuper;
	private String apesuper;
	private int dnisuper;
	private int celsuper;
	public Supervisor() {
		
	}
	public Supervisor(int codsuper, String nomsuper, String apesuper, int dnisuper, int celsuper) {
		super();
		this.codsuper = codsuper;
		this.nomsuper = nomsuper;
		this.apesuper = apesuper;
		this.dnisuper = dnisuper;
		this.celsuper = celsuper;
	}
	public int getCodsuper() {
		return codsuper;
	}
	public void setCodsuper(int codsuper) {
		this.codsuper = codsuper;
	}
	public String getNomsuper() {
		return nomsuper;
	}
	public void setNomsuper(String nomsuper) {
		this.nomsuper = nomsuper;
	}
	public String getApesuper() {
		return apesuper;
	}
	public void setApesuper(String apesuper) {
		this.apesuper = apesuper;
	}
	public int getDnisuper() {
		return dnisuper;
	}
	public void setDnisuper(int dnisuper) {
		this.dnisuper = dnisuper;
	}
	public int getCelsuper() {
		return celsuper;
	}
	public void setCelsuper(int celsuper) {
		this.celsuper = celsuper;
	}
	
	
	
	
	
	
		
	
	
	
	
}
